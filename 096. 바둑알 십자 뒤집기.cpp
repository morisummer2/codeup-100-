#include<iostream>
using namespace std;

int main() 
{

	int arr[19][19] = { 0, };

	int n,x,y;

	for (int i = 0; i < 19; i++) 
	{
		for (int j = 0; j < 19; j++) 
		{
			cin >> arr[i][j];
		}
	}

	cin >> n;
	
	for (int i = 0; i < n; i++) {
		cin >> x >> y;
		
		for (int j = 0; j < 19; j++) 
		{

			if (arr[x - 1][j])
				arr[x - 1][j] = 0;
			else
				arr[x - 1][j] = 1;

			if (arr[j][x-1])
				arr[j][x-1] = 0;
			else
				arr[j][x-1] = 1;

		}

	}

	for (int i = 0; i < 19; i++)
	{
		for (int j = 0; j < 19; j++)
		{

			cout << arr[i][j]<<' ';

		}
		cout << endl;
	}

}








